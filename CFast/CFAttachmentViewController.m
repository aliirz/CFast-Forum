}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSDictionary *attachment = [self.attachments objectAtIndex:[indexPath row]];
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier];
    
        cell.textLabel.textColor = [UIColor pumpkinColor];
        cell.backgroundColor = [UIColor cloudsColor];
        cell.textLabel.text = [attachment objectForKey:@"FILENAME"];
    }
    
    return cell;
}


